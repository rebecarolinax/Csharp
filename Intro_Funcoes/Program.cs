static float Soma(float n1, float n2)
{
    float S = n1 + n2;
    return S;
}

float resultado1 = Soma(10,2);
Console.WriteLine($"Resultado: {resultado1}");



static float Dividir(float n1, float n2)
{
    float D = n1 / n2;
    return D;
}

float resultado2 = Dividir(10, 2);
Console.WriteLine($"Resultado: {resultado2}");


static float Subtrair(float n1, float n2)
{
    float S = n1 - n2;
    return S;
}

float resultado3 = Subtrair(10,2);
Console.WriteLine($"Resultado: {resultado3}");


static float Multiplicar(float n1, float n2)
{
    float M = n1 * n2;
    return M;
}

float resultado4 = Multiplicar(10,2);
Console.WriteLine($"Resultado: {resultado4}");
