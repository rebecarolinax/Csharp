//Nada mais é do que a capacidade de um objeto que seja compatível se passe por outro em determinados momentos
//Na prática é a reescrita de um método herdado de uma classe se utilizar comportamentos da classe pai

//ENCAPSULAMENTO em POO:
//Capacidade de esconder detalhes, expondo só o que deve ser acessado publicamente
//Dessa forma é possível programar trazendo segurança na aplicação

